CREATE TABLE Vuelos
(
    ID_AEROLINEA int,
    ID_AEROPUERTO int ,
    ID_MOVIMIENTO int,
    DIA date,

    CONSTRAINT FK_AEROLINEA
    FOREIGN KEY (ID_AEROLINEA) REFERENCES Aerolineas(id_aerolinea)
    ON UPDATE CASCADE
    ON DELETE CASCADE,

    CONSTRAINT FK_AEROPUERTO
    FOREIGN KEY (ID_AEROPUERTO) REFERENCES Aerolineas(id_aeropuerto)
    ON UPDATE CASCADE
    ON DELETE CASCADE,

    CONSTRAINT FK_MOVIMIENTO
    FOREIGN KEY (ID_MOVIMIENTO) REFERENCES Aerolineas(id_movimiento)
    ON UPDATE CASCADE
    ON DELETE CASCADE
);

